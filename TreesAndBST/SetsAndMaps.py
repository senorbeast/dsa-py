## Sets and Maps are commonly implemented in BST

## Sets

# We can implement Set with Array
# But with BST Set,
# We can insert and delete elements in O(logn) time.


## Maps

# Eg. Phonebook, with names in alphabetical order.
# And every name is mapped to some phone number

# Shinde ---> 4343
# Shah ----> 2989
# Thackeray ---> 9023

# We map a key to a value.
# And the Data Structure is sorted as per the key.

# We can search a sorted phonebook in logn time.

## Although Sets and Maps can be implemted with diff DS too, HashSets and HashMaps.
## For Sets and Maps implemented with BST, they are called OrderedSet or TreeSet, OrderedMap or TreeMap.
